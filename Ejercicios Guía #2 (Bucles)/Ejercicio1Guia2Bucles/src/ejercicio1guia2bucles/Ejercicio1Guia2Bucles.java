public class Ejercicio1Guia2Bucles {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        /**
         1) Realizar un pseudocódigo que permita generar los primeros
         100 números enteros a partir de 0 e imprimir.
         * 
         * Datos salida: primeros 100 números enteros
         * Datos entrada: incremento de los números con el iterador
         **/
        int i = 0;
        while (i <= 100) {
            System.out.println(i);
            i++;
        }
    }
    
}
